// As this is a strictly frontend-only application, we don't have backend API routes.
// We export an empty api object to satisfy any default imports from the template.
export const api = {};

export function buildUrl(path: string, params?: Record<string, string | number>): string {
  let url = path;
  if (params) {
    Object.entries(params).forEach(([key, value]) => {
      if (url.includes(`:${key}`)) {
        url = url.replace(`:${key}`, String(value));
      }
    });
  }
  return url;
}
